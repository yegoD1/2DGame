public class Character {
    private int xLoc;
    private int yLoc;

    public Character(int startXLoc, int startYLoc)
    {
        xLoc = startXLoc;
        yLoc = startYLoc;
    }

    public int getXLoc()
    {
        return xLoc;
    }

    public int getYLoc()
    {
        return yLoc;
    }
}
